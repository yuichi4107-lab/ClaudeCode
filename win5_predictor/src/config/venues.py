# ──────────────────────────────────────
# 距離カテゴリ
# ──────────────────────────────────────
def distance_category(distance_m: int) -> str:
    """距離をカテゴリに変換する"""
    if distance_m <= 1400:
        return "sprint"
    elif distance_m <= 1800:
        return "mile"
    elif distance_m <= 2200:
        return "intermediate"
    elif distance_m <= 2800:
        return "long"
    else:
        return "extended"


